# 1. Удалить все неалфавитные символы и привести строку к нижнему регистру:
#   - Это необходимо для того, чтобы игнорировать пробелы, знаки препинания и регистр букв при проверке.

# 2. Сравнить строку с перевернутой версией самой себя:
#    - Если строка равна своей перевернутой версии, значит, она является палиндромом.

def is_palindrome(s):
    """
    Проверяет, является ли строка палиндромом.

    :param s: Входная строка
    :return: True, если строка является палиндромом, False в противном случае
    """
    # 1. Приводим строку к нижнему регистру и оставляем только алфавитные символы
    cleaned_str = ''.join(char.lower() for char in s if char.isalnum())

    # 2. Сравниваем очищенную строку с ее перевернутой версией
    return cleaned_str == cleaned_str[::-1]


# Примеры использования
print(is_palindrome("A man, a plan, a canal, Panama"))  # True
print(is_palindrome("No lemon, no melon"))  # True
print(is_palindrome("Hello, World!"))  # False